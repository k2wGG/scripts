check_for_script_update() {
    local local_file="$0"
    local tmp_file="/tmp/hyperlane_manager.sh.new"
    curl -s -o "$tmp_file" "$REMOTE_SCRIPT_URL" || {
        log_info "–ù–µ —É–¥–∞–ª–æ—Å—å –ø—Ä–æ–≤–µ—Ä–∏—Ç—å –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å–∫—Ä–∏–ø—Ç–∞."
