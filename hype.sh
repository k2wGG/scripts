    read -r PRIVATE_KEY

    # –°–æ–∑–¥–∞–Ω–∏–µ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –¥–ª—è –Ω–æ–¥—ã
    mkdir -p "$BASE_DIR"
    chmod -R 777 "$BASE_DIR"

    echo -e "${ICON_WAIT} –ó–∞–ø—É—Å–∫ Docker –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–∞..."
