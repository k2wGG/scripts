        log_info "–ù–∞–π–¥–µ–Ω–∞ –Ω–æ–≤–∞—è –≤–µ—Ä—Å–∏—è —Å–∫—Ä–∏–ø—Ç–∞. –û–±–Ω–æ–≤–ª—è—é—Å—å..."
