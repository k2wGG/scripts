install_node() {
    clear
    log_info "–ù–∞—á–∞–ª–æ —É—Å—Ç–∞–Ω–æ–≤–∫–∏ –Ω–æ–¥—ã Hyperlane..."
    
    echo -e "${ICON_WAIT} –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å–∏—Å—Ç–µ–º—ã..."
