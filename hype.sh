    fi

    echo -e "${ICON_WAIT} –ó–∞–≥—Ä—É–∑–∫–∞ Docker-–æ–±—Ä–∞–∑–∞..."
    docker pull --platform linux/amd64 ${DOCKER_IMAGE}

    # –í–≤–æ–¥ –¥–∞–Ω–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    echo -ne "${YELLOW}–í–≤–µ–¥–∏—Ç–µ –∏–º—è –≤–∞–ª–∏–¥–∞—Ç–æ—Ä–∞:${NC} "
    read -r NAME
